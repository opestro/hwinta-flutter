Iterable<AlgeriaState> getAllStates({AlgeriaScript lang = AlgeriaScript.arabic}) {
  late final Set<String> src;
  if (lang == AlgeriaScript.arabic) {
    src = _wilayatSortedArabic;
  } else {
    src = _wilayatSortedLatin;
  }
  var states = <AlgeriaState>[];
  for (int i = 0; i < src.length; i++) {
    states.add((
      name: src.elementAt(i),
      code: (i + 1).toString().padLeft(2, '0'),
    ));
  }
  return states;
}

AlgeriaState getStateByIndex({required int index, AlgeriaScript lang = AlgeriaScript.arabic}) {
  if (lang == AlgeriaScript.arabic) {
    return (
      name: _wilayatSortedArabic.elementAt(index),
      code: (index + 1).toString().padLeft(2, '0')
    );
  } else {
    return (
      name: _wilayatSortedLatin.elementAt(index),
      code: (index + 1).toString().padLeft(2, '0')
    );
  }
}

/// code is just index+1 and can be in to formats "1" or "01"
AlgeriaState? getStateByCode({required String code, AlgeriaScript lang = AlgeriaScript.arabic}) {
  var codeInt = int.tryParse(code);
  if (codeInt == null || codeInt < 1 || codeInt > 58) {
    // throw ArgumentError('Invalid state code');
    return null;
  }
  int index = codeInt - 1;
  if (lang == AlgeriaScript.arabic) {
    return (
      name: _wilayatSortedArabic.elementAt(index),
      code: code
    );
  } else {
    return (
      name: _wilayatSortedLatin.elementAt(index),
      code: code
    );
  }
}

// the name can be in arabic or latin, if latin can be in deffrent case, we convertit to sentance case
AlgeriaState getStateByName({required String name, AlgeriaScript lang = AlgeriaScript.arabic}) {
  if (lang == AlgeriaScript.arabic) {
    for (int i = 0; i < _wilayatSortedArabic.length; i++) {
      if (_wilayatSortedArabic.elementAt(i).sentenceCase == name.sentenceCase) {
        return (
          name: _wilayatSortedArabic.elementAt(i),
          code: (i + 1).toString().padLeft(2, '0')
        );
      }
    }
  } else {
    for (int i = 0; i < _wilayatSortedLatin.length; i++) {
      if (_wilayatSortedLatin.elementAt(i).sentenceCase == name.sentenceCase) {
        return (
          name: _wilayatSortedLatin.elementAt(i),
          code: (i + 1).toString().padLeft(2, '0')
        );
      }
    }
  }
  throw ArgumentError('State name not found');
}

// cities
Iterable<AlgeriaCity> getCitiesByStateCode({required String code, AlgeriaScript lang = AlgeriaScript.arabic}) {
  var codeInt = int.tryParse(code);
  if (codeInt == null || codeInt < 1 || codeInt > 58) {
    // throw ArgumentError('Invalid state code');
    return [];
  }
  int stateIndex = codeInt - 1;
  late final Set<String> src;
  if (lang == AlgeriaScript.arabic) {
    src = _citiesSortedArabic.elementAt(stateIndex);
  } else {
    src = _citiesSortedLatin.elementAt(stateIndex);
  }
  var cities = <AlgeriaCity>[];
  for (int i = 0; i < src.length; i++) {
    cities.add(
      (
        name: src.elementAt(i),
        code: (i + 1).toString().padLeft(2, '0'),
        stateCode: code,
      ),
    );
  }
  return cities;
}

// get city by code and state code
AlgeriaCity? getCityByCode({required String code, required String stateCode, AlgeriaScript lang = AlgeriaScript.arabic}) {
  int stateIndex = (int.tryParse(stateCode) ?? 1) - 1;
  int cityIndex = (int.tryParse(code) ?? 1) - 1;
  late final Set<String> src;
  if (lang == AlgeriaScript.arabic) {
    src = _citiesSortedArabic.elementAt(stateIndex);
  } else {
    src = _citiesSortedLatin.elementAt(stateIndex);
  }
  if (cityIndex < 0 || cityIndex >= src.length) {
    return null;
  }
  return (
    name: src.elementAt(cityIndex),
    code: code,
    stateCode: stateCode,
  );
}

enum AlgeriaScript {
  arabic,
  latin
}

